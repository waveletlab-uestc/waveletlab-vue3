function getStoredLocale() {
  if (typeof window === 'undefined') return defaultLocale

  const storedLocale = window.localStorage.getItem(localeStorageKey)
  return supportedLocales.includes(storedLocale) ? storedLocale : defaultLocale
}

export const locale = ref(getStoredLocale())

function resolveMessage(path) {
  return path.split('.').reduce((current, key) => current?.[key], messages[locale.value])
}

export function t(path) {
  const value = resolveMessage(path)
  return typeof value === 'string' ? value : path
}

export function tm(path) {
  return resolveMessage(path)
}

export function setLocale(nextLocale) {
  if (supportedLocales.includes(nextLocale)) {
    locale.value = nextLocale
  }
}

export function toggleLocale() {
  setLocale(locale.value === 'en' ? 'zh' : 'en')
}

watch(
  locale,
  (nextLocale) => {
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(localeStorageKey, nextLocale)
    }

    if (typeof document !== 'undefined') {
      document.documentElement.lang = nextLocale === 'zh' ? 'zh-CN' : 'en'
    }
  },
  { immediate: true },
)

export function useI18n() {
  return {
    locale,
    localeName: computed(() => messages[locale.value].localeName),
    localeSwitchLabel: computed(() => messages[locale.value].localeSwitchLabel),
    setLocale,
    t,
    tm,
    toggleLocale,
  }
}
